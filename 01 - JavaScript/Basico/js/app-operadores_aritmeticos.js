/* Operadores: Aritméticos e Aribuição  */

let x = 11
let y = 5
let resultado

//somar
resultado = x + y
console.log("SOMA = "+resultado)

//subtrair
resultado = x - y
console.log("SUBTRAIR = "+resultado)

//dividir
resultado = x / y
console.log("DIVIDIR = "+resultado)

//mutiplicar
resultado = x * y
console.log("MULTIPLICAR = "+resultado)

//resto da divisão
resultado = x % y
console.log("RESTO DA DIVISÃO = "+resultado)

//potencia ex:2³
resultado = x ** y
console.log("POTENCIA  = "+resultado)

//abreviação
x += y //x = x+y
x -= y //x = x-y
x *= y //x = x*y
x /= y //x = x/y
x **= y //x = x**y
x %= y //x = x%y

// incrementos
x++ // adiciona mais 1 valor no valor original

// Decrementos
x-- // tira 1 valor do valor original
